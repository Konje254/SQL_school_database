-- School database schema + sample data
-- Generated for import into MySQL Workbench
-- Contains ~300 students, staff, classes, fees, transactions and triggers
SET FOREIGN_KEY_CHECKS=0;
DROP DATABASE IF EXISTS school_db_sample;
CREATE DATABASE school_db_sample;
USE school_db_sample;
SET FOREIGN_KEY_CHECKS=1;


-- === Academic Years ===
CREATE TABLE academic_years (
    year_id INT PRIMARY KEY AUTO_INCREMENT,
    year_label VARCHAR(20) NOT NULL,
    start_date DATE,
    end_date DATE
);

-- === Guardians ===
CREATE TABLE guardians (
    guardian_id INT PRIMARY KEY AUTO_INCREMENT,
    full_name VARCHAR(100) NOT NULL,
    phone VARCHAR(20),
    email VARCHAR(100),
    relationship VARCHAR(50)
);

-- === Students ===
CREATE TABLE students (
    student_id INT PRIMARY KEY AUTO_INCREMENT,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    birth_date DATE,
    gender ENUM('Male','Female','Other'),
    phone VARCHAR(20),
    address VARCHAR(255),
    city VARCHAR(100),
    guardian_id INT,
    FOREIGN KEY (guardian_id) REFERENCES guardians(guardian_id)
);

-- === Classes ===
CREATE TABLE classes (
    class_id INT PRIMARY KEY AUTO_INCREMENT,
    class_name VARCHAR(50) NOT NULL,
    academic_year_id INT,
    FOREIGN KEY (academic_year_id) REFERENCES academic_years(year_id)
);

-- === Staff Roles ===
CREATE TABLE staff_roles (
    role_id INT PRIMARY KEY AUTO_INCREMENT,
    title VARCHAR(100) NOT NULL,
    base_salary DECIMAL(10,2)
);

-- === Staff (Unified) ===
CREATE TABLE staff (
    staff_id INT PRIMARY KEY AUTO_INCREMENT,
    role_id INT NOT NULL,
    full_name VARCHAR(100) NOT NULL,
    phone VARCHAR(20),
    email VARCHAR(100),
    hire_date DATE,
    status ENUM('Active','Inactive'),
    FOREIGN KEY (role_id) REFERENCES staff_roles(role_id)
);

-- === Class Assignments ===
CREATE TABLE class_assignments (
    assignment_id INT PRIMARY KEY AUTO_INCREMENT,
    class_id INT NOT NULL,
    teacher_id INT NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE,
    FOREIGN KEY (class_id) REFERENCES classes(class_id),
    FOREIGN KEY (teacher_id) REFERENCES staff(staff_id)
);

-- === Subjects ===
CREATE TABLE subjects (
    subject_id INT PRIMARY KEY AUTO_INCREMENT,
    subject_name VARCHAR(100) NOT NULL,
    teacher_id INT,
    FOREIGN KEY (teacher_id) REFERENCES staff(staff_id)
);

-- === Enrollments ===
CREATE TABLE enrollments (
    enrollment_id INT PRIMARY KEY AUTO_INCREMENT,
    student_id INT NOT NULL,
    class_id INT NOT NULL,
    enrollment_date DATE NOT NULL,
    FOREIGN KEY (student_id) REFERENCES students(student_id),
    FOREIGN KEY (class_id) REFERENCES classes(class_id)
);

-- === Grades ===
CREATE TABLE grades (
    grade_id INT PRIMARY KEY AUTO_INCREMENT,
    student_id INT NOT NULL,
    subject_id INT NOT NULL,
    class_id INT NOT NULL,
    term VARCHAR(20),
    score DECIMAL(5,2),
    grade CHAR(2),
    FOREIGN KEY (student_id) REFERENCES students(student_id),
    FOREIGN KEY (subject_id) REFERENCES subjects(subject_id),
    FOREIGN KEY (class_id) REFERENCES classes(class_id)
);

-- === Attendance ===
CREATE TABLE attendance (
    attendance_id INT PRIMARY KEY AUTO_INCREMENT,
    student_id INT NOT NULL,
    class_id INT NOT NULL,
    attendance_date DATE NOT NULL,
    status ENUM('Present','Absent','Late'),
    FOREIGN KEY (student_id) REFERENCES students(student_id),
    FOREIGN KEY (class_id) REFERENCES classes(class_id)
);

-- === Performance Ratings ===
CREATE TABLE performance_ratings (
    rating_id INT PRIMARY KEY AUTO_INCREMENT,
    rating_name VARCHAR(50) NOT NULL,
    percentage_increase DECIMAL(5,2) NOT NULL
);

-- === Staff Performance ===
CREATE TABLE staff_performance (
    perf_id INT PRIMARY KEY AUTO_INCREMENT,
    staff_id INT NOT NULL,
    rating_id INT NOT NULL,
    review_date DATE,
    previous_salary DECIMAL(10,2),
    FOREIGN KEY (staff_id) REFERENCES staff(staff_id),
    FOREIGN KEY (rating_id) REFERENCES performance_ratings(rating_id)
);

-- === Salary Payments ===
CREATE TABLE salary_payments (
    salary_id INT PRIMARY KEY AUTO_INCREMENT,
    staff_id INT NOT NULL,
    month_year VARCHAR(7) NOT NULL,
    amount DECIMAL(10,2) NOT NULL,
    payment_date DATE NOT NULL,
    performance_based BOOLEAN,
    FOREIGN KEY (staff_id) REFERENCES staff(staff_id)
);

-- === Fee Structures ===
CREATE TABLE fee_structures (
    fee_id INT PRIMARY KEY AUTO_INCREMENT,
    class_id INT NOT NULL,
    academic_year VARCHAR(20),
    term VARCHAR(20),
    amount DECIMAL(10,2),
    FOREIGN KEY (class_id) REFERENCES classes(class_id)
);

-- === Student Fees ===
CREATE TABLE student_fees (
    payment_id INT PRIMARY KEY AUTO_INCREMENT,
    student_id INT NOT NULL,
    fee_id INT NOT NULL,
    amount_paid DECIMAL(10,2),
    payment_date DATE,
    method ENUM('Cash','Mobile Money','Bank Transfer','Card'),
    FOREIGN KEY (student_id) REFERENCES students(student_id),
    FOREIGN KEY (fee_id) REFERENCES fee_structures(fee_id)
);

-- === Vendors ===
CREATE TABLE vendors (
    vendor_id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(100) NOT NULL,
    contact_info VARCHAR(255)
);

-- === Procurements ===
CREATE TABLE procurements (
    procurement_id INT PRIMARY KEY AUTO_INCREMENT,
    vendor_id INT NOT NULL,
    description TEXT,
    amount DECIMAL(10,2),
    date DATE,
    FOREIGN KEY (vendor_id) REFERENCES vendors(vendor_id)
);

-- === Transactions Ledger ===
CREATE TABLE transactions (
    transaction_id INT PRIMARY KEY AUTO_INCREMENT,
    type ENUM('Revenue','Expense') NOT NULL,
    category VARCHAR(50),
    amount DECIMAL(10,2) NOT NULL,
    date DATE NOT NULL,
    source_id INT,
    reference_no VARCHAR(50)
);


-- === Triggers: auto-insert into transactions for certain events ===
DELIMITER $$

CREATE TRIGGER trg_after_student_fee_insert
AFTER INSERT ON student_fees
FOR EACH ROW
BEGIN
  INSERT INTO transactions (type, category, amount, date, source_id, reference_no)
  VALUES ('Revenue', 'School Fees', NEW.amount_paid, NEW.payment_date, NEW.payment_id, CONCAT('FEE-', NEW.payment_id));
END$$

CREATE TRIGGER trg_after_salary_payment_insert
AFTER INSERT ON salary_payments
FOR EACH ROW
BEGIN
  INSERT INTO transactions (type, category, amount, date, source_id, reference_no)
  VALUES ('Expense', 'Salary Payment', NEW.amount, NEW.payment_date, NEW.salary_id, CONCAT('SAL-', NEW.salary_id));
END$$

CREATE TRIGGER trg_after_procurement_insert
AFTER INSERT ON procurements
FOR EACH ROW
BEGIN
  INSERT INTO transactions (type, category, amount, date, source_id, reference_no)
  VALUES ('Expense', 'Procurement', NEW.amount, NEW.date, NEW.procurement_id, CONCAT('PRC-', NEW.procurement_id));
END$$

DELIMITER ;

